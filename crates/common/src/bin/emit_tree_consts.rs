//! Emit the on-chain Merkle tree constants (empty-subtree hashes + empty root)
//! as a Rust source file, so the program doesn't recompute 24 Poseidon hashes
//! per insert. Generated with light-poseidon (== sol_poseidon, proven in M0).
//!
//! Usage: emit_tree_consts <out_file.rs>

use std::{fs, path::PathBuf};

use opaq_common::{
    poseidon_hash2_be,
    tree::{empty_root, zero_hashes, TREE_DEPTH},
};

fn arr(b: &[u8; 32]) -> String {
    let items: Vec<String> = b.iter().map(|x| x.to_string()).collect();
    format!("[{}]", items.join(", "))
}

fn main() {
    let out = PathBuf::from(std::env::args().nth(1).expect("arg1: out file"));
    let h = |a: &[u8; 32], b: &[u8; 32]| poseidon_hash2_be(a, b);
    let zeros = zero_hashes(&h);
    let root = empty_root(&h, &zeros);

    let zlits: Vec<String> = zeros.iter().map(arr).collect();
    let src = format!(
        "// @generated by `emit_tree_consts` — do not edit.\n\
         // Empty-subtree hashes (Poseidon, original/Circom params) for the depth-{d} tree.\n\
         pub const TREE_DEPTH: usize = {d};\n\
         pub const ZEROS: [[u8; 32]; {d}] = [{}];\n\
         pub const EMPTY_ROOT: [u8; 32] = {};\n",
        zlits.join(", "),
        arr(&root),
        d = TREE_DEPTH,
    );
    fs::write(&out, src).unwrap();
    println!("wrote {} (EMPTY_ROOT = 0x{})", out.display(), hex::encode(root));
}

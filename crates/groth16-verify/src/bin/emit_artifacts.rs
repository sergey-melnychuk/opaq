//! Emit on-chain artifacts for the M3 SBF verifier from a circuit's snarkjs
//! Groth16 output: a Rust `vk.rs` (VK byte consts to embed in the program) and
//! `instruction.bin` (proof_a‖proof_b‖proof_c‖public_inputs) for the client.
//!
//! Usage: emit_artifacts <groth16_dir> <out_dir>

use std::{fs, path::PathBuf};

use groth16_verify::{proof_from_json, public_from_json, vk_from_json};
use serde_json::Value;

fn bytes_lit(b: &[u8]) -> String {
    let items: Vec<String> = b.iter().map(|x| x.to_string()).collect();
    format!("[{}]", items.join(", "))
}

fn main() {
    let args: Vec<String> = std::env::args().collect();
    let groth_dir = PathBuf::from(args.get(1).expect("arg1: groth16 dir"));
    let out_dir = PathBuf::from(args.get(2).expect("arg2: out dir"));
    let read = |n: &str| -> Value {
        serde_json::from_str(&fs::read_to_string(groth_dir.join(n)).unwrap()).unwrap()
    };

    let vk = vk_from_json(&read("verification_key.json"));
    let proof = proof_from_json(&read("proof.json"));
    let public = public_from_json(&read("public.json"));

    let ic_items: Vec<String> = vk.ic.iter().map(|p| bytes_lit(p)).collect();
    let vk_rs = format!(
        "// @generated by `emit_artifacts` — do not edit.\n\
         // INSECURE test vk (trivial ceremony) — see OPAQ.md B.6 SECURITY CAVEAT.\n\
         pub const VK_ALPHA_G1: [u8; 64] = {};\n\
         pub const VK_BETA_G2: [u8; 128] = {};\n\
         pub const VK_GAMME_G2: [u8; 128] = {};\n\
         pub const VK_DELTA_G2: [u8; 128] = {};\n\
         pub const VK_IC: [[u8; 64]; {}] = [{}];\n",
        bytes_lit(&vk.alpha),
        bytes_lit(&vk.beta),
        bytes_lit(&vk.gamma),
        bytes_lit(&vk.delta),
        vk.ic.len(),
        ic_items.join(", "),
    );
    fs::write(out_dir.join("vk.rs"), vk_rs).unwrap();

    let mut data = Vec::with_capacity(256 + public.len() * 32);
    data.extend_from_slice(&proof.a);
    data.extend_from_slice(&proof.b);
    data.extend_from_slice(&proof.c);
    for p in &public {
        data.extend_from_slice(p);
    }
    fs::write(out_dir.join("instruction.bin"), &data).unwrap();

    println!(
        "wrote vk.rs (nr_pubinputs={}) and instruction.bin ({} bytes)",
        vk.ic.len() - 1,
        data.len()
    );
}
